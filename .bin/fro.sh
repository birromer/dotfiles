#!/bin/sh
#
# fro: an xmas ascii banner generator for my bro
# MMXV xero (http://xero.nu)

usage() {
printf "usage: `basename $0` \n\
   [-n --normal normal] \n\
   [-d --dots dots] \n\
   [-c --color color] \n\
   [-h --help help]\n"
}

dots() {
cat << FRO

 .........................................
 .........................................
 ........ _________ ......................
 ........ \_       \______ .. ______ .....
 ......... /              \  /      \ ....
 ........ /   ___  /  /   /\/  _    /\ ...
 ....... /   /   \/ _/  _/ /   /   /  \ ..
 ...... /   /__ /   \   \ /   /   /   / ..
 ..... /   _  //   //   //   /   /   / ...
 .... /   / \//   /_   //   /   /   / ....
 .. _/   /   /_____/   \       /   / .....
 .. \___/  /\    /_____/\_____/   / ......
 ... \    /..\___\       \       / .......
 .... \__/ ...... \______/\_____/ ........
 .........................................
 .........................................

FRO
}

normal() {
cat << FRO
      _________                       
      \_       \______    ______      
       /              \  /      \     
      /   ___  /  /   /\/  _    /\    
     /   /   \/ _/  _/ /   /   /  \   
    /   /__ /   \   \ /   /   /   /   
   /   _  //   //   //   /   /   /    
  /   / \//   /_   //   /   /   /     
_/   /   /_____/   \       /   /      
\___/  /\    /_____/\_____/   /       
 \    /  \___\       \       /        
  \__/        \______/\_____/         
FRO
}

color() {
cat << FRO

 [1;33m.........................................
 [1;33m.........................................
 [1;33m........ [1;34m_________ [1;33m......................
 [1;33m........ [1;34m\_       \______ [1;33m.. [1;34m______ [1;33m.....
 [1;33m......... [1;34m/              \  /      \ [1;33m....
 [1;33m........ [1;34m/   ___  /  /   /[1;35m\[1;34m/  _    /[1;35m\ [1;33m...
 [1;33m....... [1;34m/   /   \/ _/  _/ /   /   /  [1;35m\ [1;33m..
 [1;33m...... [1;34m/   /__ /   \   \ /   /   /   [1;35m/ [1;33m..
 [1;33m..... [1;34m/   _  //   //   //   /   /   [1;35m/ [1;33m...
 [1;33m.... [1;34m/   / \//   /_   //   /   /   [1;35m/ [1;33m....
 [1;33m.. [1;34m_/   /   /_____/   \       /   [1;35m/ [1;33m.....
 [1;33m.. [1;34m\___/  [1;35m/\    [1;34m/_____/\_____/   [1;35m/ [1;33m......
 [1;33m... [1;35m\    /[1;33m..[1;35m\___\       \       / [1;33m.......
 [1;33m.... [1;35m\__/ [1;33m...... [1;35m\______/\_____/ [1;33m........
 [1;33m.........................................
 [1;33m.........................................

FRO
}

case "$1" in
  *-d|--dots|dots*)
    dots
  ;;
  *-c|--color|color*)
    color
  ;;
  *-n|--normal|normal)
    normal
  ;;
  *)
    normal
    usage
  ;;
esac
